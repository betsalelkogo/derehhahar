function getNested(obj, path) {
  return path.split(".").reduce((o, k) => o?.[k], obj);
}

function escapeHtml(text) {
  return String(text)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function renderRoutes(routesSection) {
  const grid = document.getElementById("routes-grid");
  if (!grid || !routesSection?.tours?.length) {
    return;
  }

  grid.innerHTML = routesSection.tours
    .map((tour) => {
      const descHtml = tour.html
        ? tour.desc
        : escapeHtml(tour.desc);
      const noteHtml = tour.note
        ? `<p class="route-note">${escapeHtml(tour.note)}</p>`
        : "";

      return `
        <article class="route-card">
          <div class="route-card-photo route-card-photo--${tour.photoClass}">
            <img src="images/${tour.image}" alt="${escapeHtml(tour.alt)}" loading="lazy">
          </div>
          <div class="route-card-body">
            <span class="route-badge">${escapeHtml(tour.badge)}</span>
            <h3>${escapeHtml(tour.name)}</h3>
            <p>${descHtml}</p>
            ${noteHtml}
          </div>
        </article>`;
    })
    .join("");
}

window.renderRoutes = renderRoutes;

function setLanguage(lang) {
  if (!I18N[lang]) {
    return;
  }

  const t = I18N[lang];
  const isRtl = lang === "he";

  document.documentElement.lang = lang === "he" ? "he" : lang;
  document.documentElement.dir = isRtl ? "rtl" : "ltr";

  document.title = t.meta.title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) {
    metaDesc.setAttribute("content", t.meta.description);
  }

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = getNested(t, key);
    if (val != null) {
      el.textContent = val;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const val = getNested(t, key);
    if (val != null) {
      el.innerHTML = val;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const val = getNested(t, key);
    if (val != null) {
      el.placeholder = val;
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    const val = getNested(t, key);
    if (val != null) {
      el.setAttribute("aria-label", val);
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
    const key = el.getAttribute("data-i18n-alt");
    if (/^\d+$/.test(key)) {
      el.alt = t.gallery.alts[parseInt(key, 10)] || "";
    } else {
      const val = getNested(t, key);
      if (val != null) {
        el.alt = val;
      }
    }
  });

  const mainNav = document.querySelector(".header-nav");
  if (mainNav) {
    mainNav.setAttribute("aria-label", t.nav.ariaMain);
  }

  const lbClose = document.getElementById("lightbox-close");
  const lbPrev = document.getElementById("lightbox-prev");
  const lbNext = document.getElementById("lightbox-next");
  if (lbClose) {
    lbClose.setAttribute("aria-label", t.lightbox.close);
  }
  if (lbPrev) {
    lbPrev.setAttribute("aria-label", t.lightbox.prev);
    lbPrev.textContent = isRtl ? "→" : "‹";
  }
  if (lbNext) {
    lbNext.setAttribute("aria-label", t.lightbox.next);
    lbNext.textContent = isRtl ? "←" : "›";
  }

  renderRoutes(t.routes);

  const marqueeTrack = document.querySelector(".marquee-track");
  if (marqueeTrack) {
    const items = [...t.marquee, ...t.marquee];
    marqueeTrack.innerHTML = items.map((text) => `<span>${text}</span>`).join("");
  }

  const waText = encodeURIComponent(t.whatsappText);
  const waBase = `https://api.whatsapp.com/send?phone=972549149646&text=${waText}`;
  document.querySelectorAll("[data-wa-link]").forEach((el) => {
    el.href = waBase;
  });

  const subjectInput = document.querySelector('input[name="subject"]');
  const fromInput = document.querySelector('input[name="from_name"]');
  if (subjectInput) {
    subjectInput.value = t.form.subject;
  }
  if (fromInput) {
    fromInput.value = t.form.fromName;
  }

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("lang-btn--active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang ? "true" : "false");
  });

  localStorage.setItem("derehhahar-lang", lang);
  window.currentLang = lang;
}

function initI18n() {
  const saved = localStorage.getItem("derehhahar-lang");
  const browserLang = navigator.language?.slice(0, 2);
  const initial = saved || (I18N[browserLang] ? browserLang : "he");
  setLanguage(initial);

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });
}

window.I18N = I18N;
window.setLanguage = setLanguage;
window.getI18n = () => I18N[window.currentLang || "he"];
